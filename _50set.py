set1={1,2,3,4,5}
set2={4,5,6,7,8}

print("Set 1 : ",set1)
print("Set 2 : ",set2)

unionSet=set1.union(set2)
print("Union of set1 and set2 is : ",unionSet)

intersectionSet=set1.intersection(set2)
print("Intersection of set1 and set 2 is : ",intersectionSet)

differnce=set1.difference(set2)
print("Differnece of set1 and set2 : ",differnce)

